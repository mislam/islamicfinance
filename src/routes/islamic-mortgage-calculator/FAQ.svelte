	<h2 class="mb-8 text-center text-2xl font-bold sm:text-3xl">Frequently Asked Questions</h2>
	<div class="space-y-4">
		<details class="group rounded-lg border border-base-content/20 bg-base-100 p-4">
			<summary class="cursor-pointer font-semibold text-base-content hover:text-primary">
				What is Diminishing Musharaka and how does it differ from a conventional mortgage?
			</summary>
			<div class="mt-4 space-y-2 text-base-content/80">
				<p>
					<strong class="text-base-content">Diminishing Musharaka</strong>
					is a Sharia-compliant partnership financing structure where you and the financial institution
					co-own the property. You pay rent on the institution's share and gradually buy out their ownership
					through monthly buyout payments.
				</p>
				<p>
					Unlike a <strong class="text-base-content">conventional mortgage</strong>
					(which is a loan with interest), this is a true partnership where both parties share ownership
					and expenses proportionally. As your ownership increases, your share of expenses increases,
					and rent decreases.
				</p>
			</div>
		</details>

		<details class="group rounded-lg border border-base-content/20 bg-base-100 p-4">
			<summary class="cursor-pointer font-semibold text-base-content hover:text-primary">
				Is Islamic home financing just interest with a different name?
			</summary>
			<div class="mt-4 space-y-2 text-base-content/80">
				<p>
					<strong class="text-base-content">No.</strong>
					Interest (riba) is payment for the use of money, which is prohibited in Islam. In Diminishing
					Musharaka, you pay
					<strong class="text-base-content">rent</strong>
					for the use of a physical asset (the property), which is permissible.
				</p>
				<p>
					The rental rate must reflect <strong class="text-base-content">fair market value</strong>
					, not be disguised interest. The key difference: interest is payment for money, rent is payment
					for an asset.
				</p>
			</div>
		</details>

		<details class="group rounded-lg border border-base-content/20 bg-base-100 p-4">
			<summary class="cursor-pointer font-semibold text-base-content hover:text-primary">
				Why is rent allowed in Islam but interest is not?
			</summary>
			<div class="mt-4 space-y-2 text-base-content/80">
				<p>
					Islamic law distinguishes between <strong class="text-base-content">riba</strong>
					(interest on money) and
					<strong class="text-base-content">rent</strong>
					(payment for use of an asset).
				</p>
				<p>
					<strong class="text-base-content">Riba is prohibited</strong>
					because money has no intrinsic value - charging for its use creates wealth without productive
					activity. This is considered exploitative.
				</p>
				<p>
					<strong class="text-base-content">Rent is allowed</strong>
					because you're paying for the use of a real asset (property) that has value and can generate
					benefit. The rental rate must reflect fair market value, ensuring it's not disguised interest.
				</p>
			</div>
		</details>

		<details class="group rounded-lg border border-base-content/20 bg-base-100 p-4">
			<summary class="cursor-pointer font-semibold text-base-content hover:text-primary">
				Is Islamic home financing more expensive than conventional mortgages?
			</summary>
			<div class="mt-4 space-y-2 text-base-content/80">
				<p>
					Costs vary based on rental rates, buyout terms, and market conditions. In some cases,
					halal financing can be competitive or even more cost-effective, especially when
					considering:
				</p>
				<ul class="ml-6 list-disc space-y-1">
					<li>Ownership structure and shared expenses</li>
					<li>No prepayment penalties (typically)</li>
					<li>Proportional cost sharing (insurance, property tax)</li>
				</ul>
				<p>
					Use this calculator to compare total costs, payments, and net gain for your specific
					scenario.
				</p>
			</div>
		</details>

		<details class="group rounded-lg border border-base-content/20 bg-base-100 p-4">
			<summary class="cursor-pointer font-semibold text-base-content hover:text-primary">
				What happens if I can't make payments in Diminishing Musharaka?
			</summary>
			<div class="mt-4 space-y-2 text-base-content/80">
				<p>
					The terms depend on your specific contract, but typically the institution may exercise
					rights as a co-owner, such as selling the property and distributing proceeds based on
					ownership percentages.
				</p>
				<p>
					This differs from foreclosure in conventional mortgages, as both parties have ownership
					rights. Always review your specific contract terms and consult with your financial
					institution.
				</p>
			</div>
		</details>

		<details class="group rounded-lg border border-base-content/20 bg-base-100 p-4">
			<summary class="cursor-pointer font-semibold text-base-content hover:text-primary">
				Can I pay off my Islamic home financing early?
			</summary>
			<div class="mt-4 space-y-2 text-base-content/80">
				<p>
					<strong class="text-base-content">Yes,</strong>
					you can typically buy out the institution's remaining share at any time. Unlike conventional
					mortgages that may have prepayment penalties, early buyout in Diminishing Musharaka simply accelerates
					your ownership acquisition.
				</p>
				<p>
					Check your specific contract for any terms, conditions, or procedures for early buyout.
				</p>
			</div>
		</details>

		<details class="group rounded-lg border border-base-content/20 bg-base-100 p-4">
			<summary class="cursor-pointer font-semibold text-base-content hover:text-primary">
				How is ownership shared in Diminishing Musharaka?
			</summary>
			<div class="mt-4 space-y-2 text-base-content/80">
				<p>
					Ownership is <strong class="text-base-content">proportional</strong>
					to each party's investment. If you put down 20% and the institution provides 80%, you own 20%
					and they own 80% initially.
				</p>
				<p>
					As you make buyout payments, your ownership increases and theirs decreases. Expenses
					(insurance, property tax) are also shared proportionally based on ownership percentages.
					This reflects the fair sharing principle in Islamic partnerships.
				</p>
			</div>
		</details>

		<details class="group rounded-lg border border-base-content/20 bg-base-100 p-4">
			<summary class="cursor-pointer font-semibold text-base-content hover:text-primary">
				Is Diminishing Musharaka really halal and approved by Islamic scholars?
			</summary>
			<div class="mt-4 space-y-2 text-base-content/80">
				<p>
					<strong class="text-base-content">Yes,</strong>
					Diminishing Musharaka is widely accepted by Islamic scholars as a Sharia-compliant alternative
					to interest-based mortgages.
				</p>
				<p>
					It's based on the principles of <strong class="text-base-content">Musharaka</strong>
					(partnership) and
					<strong class="text-base-content">Ijarah</strong>
					(leasing), both recognized as valid Islamic finance structures. However, implementation details
					matter - the rental rate must reflect fair market value, not disguised interest.
				</p>
				<p>
					Always verify that your financial institution's implementation follows proper Sharia
					guidelines and has appropriate scholarly oversight.
				</p>
			</div>
		</details>
	</div>
